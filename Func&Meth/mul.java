public class mul {
    public static int multi(int a,int b){
        int mull=a*b;
        return mull;
    }
    public static void main(String[] args) {
        int a=5;
        int b=10;
        int prod=multi(a,b);
        System.out.println(prod);
        prod=multi(5, 20);
        System.out.println(prod);

    }
}
